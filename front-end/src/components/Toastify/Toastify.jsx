import { toast } from 'react-toastify';

export const notifySuccess = (text) => toast.success(text, {
    position: "bottom-left",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    style: {
        fontSize: "16px"
    }
});

